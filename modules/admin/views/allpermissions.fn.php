<?

//--------------------------------------------------------------------------------------------------
//|	form to display all permission sets on this site
//--------------------------------------------------------------------------------------------------

function admin_allpermissions($args) {
	global $user;
	global $kapenta;
	$html = '';				//%	return value [string]

	//----------------------------------------------------------------------------------------------
	//	check arguments and user role
	//----------------------------------------------------------------------------------------------
	if ('admin' != $user->role) { return $html; }

	//----------------------------------------------------------------------------------------------
	//	make the block
	//----------------------------------------------------------------------------------------------
	$mods = $kapenta->listModules();
	foreach($mods as $module) {	$html .= "[[:admin::managepermissions::module=$module:]]";	}

	return $html;
}

?>
