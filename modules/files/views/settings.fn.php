<?

//--------------------------------------------------------------------------------------------------
//|	makes form for changing file module registry settings
//--------------------------------------------------------------------------------------------------

function files_settings($args) {
	global $kapenta;
	global $theme;

	$html = '';							//%	return value [string]

	//----------------------------------------------------------------------------------------------
	//	check user role
	//----------------------------------------------------------------------------------------------
	if ('admin' != $kapenta->user->role) { return ''; }

	//----------------------------------------------------------------------------------------------
	//	make the block
	//----------------------------------------------------------------------------------------------
	$block = $theme->loadBlock('modules/files/views/settings.block.php');

	$labels = array();

	$html = $theme->replaceLabels($labels, $block); 

	return $html;
}

?>
