<?php

//--------------------------------------------------------------------------------------------------
//|	list all Kapenta.JS files
//--------------------------------------------------------------------------------------------------

function kjs_listallflat($args) {
	global $kapenta;
	global $kapenta;

	$html = '';							//%	return value [string]

	//----------------------------------------------------------------------------------------------
	//	check arguments and user role
	//----------------------------------------------------------------------------------------------
	if ('admin' != $kapenta->user->role) { return ''; }

	//----------------------------------------------------------------------------------------------
	//	make the block
	//----------------------------------------------------------------------------------------------
	$files = $kapenta->fileSearch("data/kjs/");
	foreach($files as $item) {
		$html .= $item . "<br/>\n";
	}

	return $html;
}

