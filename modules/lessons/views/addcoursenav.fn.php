function lessons_addcoursenav($args) {
	global $user;
	global $theme;

	$html = '';						//%	return value

	//----------------------------------------------------------------------------------------------
	//	check user role
	//----------------------------------------------------------------------------------------------
	if ($user->role != 'admin') { return ''; }

	//----------------------------------------------------------------------------------------------
	//	make the block
	//----------------------------------------------------------------------------------------------
	$html = $theme->loadBlock('modules/lessons/views/addcoursenav.block.php');
	return $html;
}

