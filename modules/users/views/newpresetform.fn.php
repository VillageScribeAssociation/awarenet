<?

//--------------------------------------------------------------------------------------------------
//|	form to create a new theme preset object
//--------------------------------------------------------------------------------------------------
//role: admin

function users_newpresetform($args) {
	global $user;
	global $theme;

	$html = '';								//%	return value [string]

	//----------------------------------------------------------------------------------------------
	//	check user role and any arguments
	//----------------------------------------------------------------------------------------------
	if ('admin' != $user->role) { return ''; }

	//----------------------------------------------------------------------------------------------
	//	make the block
	//----------------------------------------------------------------------------------------------
	$html = $theme->loadBlock('modules/users/views/newpresetform.block.php');

	return $html;
}

?>
