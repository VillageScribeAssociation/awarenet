<?

//--------------------------------------------------------------------------------------------------
//|	generates form to apply a theme preset to a specified school grade
//--------------------------------------------------------------------------------------------------

function users_presetgradeform($args) {
	global $kapenta;
	global $theme;

	$html = '';						//%	return value [string]

	//----------------------------------------------------------------------------------------------
	//	check arguments and user role
	//----------------------------------------------------------------------------------------------
	if ('admin' != $kapenta->user->role) { return ''; }

	//----------------------------------------------------------------------------------------------
	//	make the block
	//----------------------------------------------------------------------------------------------
	$html = $theme->loadBlock('modules/users/views/presetgradeform.block.php');

	return $html;
}

?>
