<?

//--------------------------------------------------------------------------------------------------
//|	generates form to apply a theme preset to a specified school
//--------------------------------------------------------------------------------------------------

function users_presetschoolform($args) {
	global $user;
	global $theme;

	$html = '';						//%	return value [string]

	//----------------------------------------------------------------------------------------------
	//	check arguments and user role
	//----------------------------------------------------------------------------------------------
	if ('admin' != $user->role) { return ''; }

	//----------------------------------------------------------------------------------------------
	//	make the block
	//----------------------------------------------------------------------------------------------
	$html = $theme->loadBlock('modules/users/views/presetschoolform.block.php');

	return $html;
}

?>
